/********买东西********/
//创建变量用来储存可用方案的次数
var a = 0;
//创建变量储存总钱数
var b = 100;
//循环 遍历
//毛巾为主的循环体
for (var i = 0; i <= (b / 5); i++) {
    //以牙刷为主的循环体
    for (var j = 0; j <= (b / 2); j++) {
        //以牙膏为主的循环体
        for (var k = 0; k <= (b / 10); k++) {
            if ((i * 5) + (j * 2) + (k * 10) === b){
                a++;
            }
        }
    }
}
//输出结果
console.log(a);